    private void cargarCuentas(){
        Conversiones conversiones = new Conversiones();
        List<List<Object>> cuentas = conversiones.obtenerCuentasCliente(idClienteEnSesion);
        if(cuentas != null && !cuentas.isEmpty()){
            DefaultTableModel model = new DefaultTableModel();
            model.addColumn("Numero de Cuenta");
            model.addColumn("Saldo");
            for(List<Object> cuenta : cuentas){
                model.addRow(cuenta.toArray());
            }
            tblCuentas.setModel(model);
        }
        else{
            JOptionPane.showMessageDialog(null, "No se encontraron cuentas para este cliente.", "Informaci√≥n", JOptionPane.INFORMATION_MESSAGE);
        }
    }

